/**
 * Helper para concentar a mecanica do jogo
 */
export class GameMechanicsHelper {
    
    
    
    initiative(): number {
        // Implementação básica de iniciativa
        // Retorna um valor aleatório entre 1 e 20 (simulando um d20)
        return Math.floor(Math.random() * 20) + 1;
    }




}

